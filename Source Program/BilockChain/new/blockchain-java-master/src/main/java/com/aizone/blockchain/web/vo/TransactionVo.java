package com.aizone.blockchain.web.vo;

/**
 * 发送交易参数 VO
 * @author yangjian
 * @since 18-4-13
 */
public class TransactionVo {
	
	/**
	 * 付款人签名
	 */
	private String sign;
	/**
	 * 比特币/以太币地址
	 */
	private String coinSendAddress;
	/**
	 * 收款人公钥
	 */
	private String publicKey;
	
	/**
	 * 交易时间戳
	 */
	private String timestamp;
	
	/**
	 * 交易 Hash 值
	 */
	private String txHash;
	
	/**
	 * 交易错误信息
	 */
	private String errorMessage;
	
	/**-----------------------------------------------------------------------------------/
	/**
	 * 付款人地址
	 */
	private String sender;
	/**
	 * 收款人地址
	 */
	private String recipient;
	/**
	 * 交易金额
	 */
	private String amount;
	/**
	 * 付款人私钥
	 */
	private String privateKey;
	/**
	 * 附加数据
	 */
	private String data;

	
	
	public String getCoinSendAddress() {
		return coinSendAddress;
	}

	public void setCoinSendAddress(String coinSendAddress) {
		this.coinSendAddress = coinSendAddress;
	}

	public String getSender() {
		return sender;
	}

	public void setSender(String sender) {
		this.sender = sender;
	}

	public String getRecipient() {
		return recipient;
	}

	public void setRecipient(String recipient) {
		this.recipient = recipient;
	}

	//BigDecimal
	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getPrivateKey() {
		return privateKey;
	}

	public void setPrivateKey(String privateKey) {
		this.privateKey = privateKey;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	//-------------------------------------------
	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getPublicKey() {
		return publicKey;
	}

	public void setPublicKey(String publicKey) {
		this.publicKey = publicKey;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getTxHash() {
		return txHash;
	}

	public void setTxHash(String txHash) {
		this.txHash = txHash;
	}

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	
}
